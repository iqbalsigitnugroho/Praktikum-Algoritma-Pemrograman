# Perulangan (loop)
# For kondisi:
# Aksi
# ini dengan list
angka2_list = [0,2,4,6,8,10] # Ini adalah list
print(angka2_list)

for i in angka2_list:
    print(f"i sekarang -> {i}")
print("akhir dari program 1 \n")

# Ini dengan range
angka2_range = range(5)
for i in angka2_range:
    print(f"i sekarang -> {i}")
print("akhir dari program 2 \n")

angka2_range = range(1,10)
for i in angka2_range:
    print(f"i sekarang -> {i}")
    print("saya keren")
print("akhir dari program 3 \n")

# Menggunakan String
data_str = "saya ganteng abisss"
